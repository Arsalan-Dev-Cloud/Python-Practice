#-----------Function with Parameters--------------
def greet(name):
    print(f"Hello, {name}")

greet("Arsalan") # Hello, Arsalan
greet("Ali") # Hello, Ali


#-----------Multiple Parameters-------------------
def student(name, age):
    print(f"Name: {name}") # Name: Arsalan
    print(f"Age: {age}") # Age: 22

student("Arsalan",22)
